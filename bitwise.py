# Logical Operators
#  or
#  and
#  no
#  & Conjunction
#  | Disjunction
#  ~ Negation
#  ^ Exclusive

print(bin(15))
print(bin(22))
print(15 & 22)
print(15 | 22)
print(~22)
print(22 >> 1)
print(22 << 1)


